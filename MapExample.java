import java.util.HashMap;

public class MapExample {
    public static void main(String[] args) {
        HashMap<String , Integer> number = new HashMap<>() ;
        number.put("one" , 1) ;
        number.put("Two" , 2) ;
        number.put("Three" , 3) ;
        number.put("Four" , 4) ;
        System.out.println(number);
    }
}

